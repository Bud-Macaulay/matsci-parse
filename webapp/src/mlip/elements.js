// Symbol -> { Z, mass } lookup
export const ELEMENT_BY_SYMBOL = {}
// Z -> mass lookup (1-indexed, Z=0 unused)
export const MASS_BY_Z = new Float64Array(119)

for (const [sym, data] of Object.entries(ELEMENTS)) {
  ELEMENT_BY_SYMBOL[sym] = data.Z
  MASS_BY_Z[data.Z] = data.mass
}

// Fallback mass for unknown elements
const DEFAULT_MASS = 12.011

export function massForZ(z) {
  return MASS_BY_Z[z] || DEFAULT_MASS
}
